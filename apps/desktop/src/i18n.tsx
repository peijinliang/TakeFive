function detectLocale(): Locale {
  const saved = window.localStorage.getItem("takefive.locale");
  if (localeOptions.some((option) => option.code === saved)) return saved as Locale;
  const browserLocale = navigator.language.toLowerCase();
  if (browserLocale.startsWith("zh")) return "zh-CN";
  if (browserLocale.startsWith("ja")) return "ja-JP";
  if (browserLocale.startsWith("es")) return "es-ES";
  return "en-US";
}

function interpolate(value: string, params?: Record<string, string | number>) {
  if (!params) return value;
  return Object.entries(params).reduce((result, [key, replacement]) => result.split(`{${key}}`).join(String(replacement)), value);
}

type I18nValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, params?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(detectLocale);
  const setLocale = (nextLocale: Locale) => {
    setLocaleState(nextLocale);
    window.localStorage.setItem("takefive.locale", nextLocale);
  };
  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);
  const value = useMemo<I18nValue>(() => ({
    locale,
    setLocale,
    t: (key, params) => interpolate(translations[locale][key] ?? translations["en-US"][key] ?? key, params),
  }), [locale]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}
